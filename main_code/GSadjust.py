            # transpose table
            g = [list(i) for i in zip(*all_g)]
            table = [unique_station_names, lat, lon, elev]
            table += g
            table += out_table_iteration
            table += out_table_cumulative
            # transpose back
            table = [list(i) for i in zip(*table)]
            table = [header] + table
            return table

